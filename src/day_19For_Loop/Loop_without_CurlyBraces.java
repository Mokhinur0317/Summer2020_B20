package day_19For_Loop;

public class Loop_without_CurlyBraces {
    public static void main(String[] args) {
        for (int i  = 5; i <= 10; i++ ){
            System.out.println("Hello");
            System.out.println("how are u");
        }

        System.out.println("=============================");
        for (int i = 1; i <= 5; i++)
            System.out.println("Hello");           // if u have loop without curly braces it only exuced only one line of code
            System.out.println("how are u");




    }
}
