package day_3_sequenses_variables;

public class Escape_Sequences {
    public static void main(String[] args) {

        System.out.println("Hello everyone  \nMy name is Moki");
// this separetes one message
        System.out.println("\ti live in San Francisco");
         // this one is paragraf spaces
        System.out.println("\n\n\n\t\t\t i love programming");

        System.out.println("\\"); // one  back slash
        System.out.println("/");// one front slash

        System.out.println("\\\\"); // two back slash
        System.out.println("//");    // two front slash


        System.out.println(" My name is \'Moki\'");// 'moki'
        System.out.println("My name is  'Moki' ");

        System.out.println("My favorite TV-show Game of Trone");
    }
}
