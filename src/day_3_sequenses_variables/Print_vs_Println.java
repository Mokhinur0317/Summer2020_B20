package day_3_sequenses_variables;

public class Print_vs_Println {
    public static void main(String[]args){
        System.out.println("a");// a + new line
        System.out.println("b");// b + new line
        System.out.println("c");// c + new line

        // this was println as u can see it printed each line

        System.out.print(" a ");//a
        System.out.print(" b ");//b    thay all same line
        System.out.print(" c ");//c
        /*
        this was just the print statement and
        it printed at the same line
         */
    }
}
