package day_18Loop;

public class For_Loop {
    public static void main(String[] args) {

       for (int i = 0; i < 10; i++){   //   0,1,2,.......10  it becaes false
           System.out.println("Hello world");
       }

        for (int name = 0; name < 15 ;++name){
            System.out.println("Moki");
        }

    }
}
