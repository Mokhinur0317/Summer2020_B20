package day_32_Methods_Overloading;

import java.util.Arrays;

public class MethodOverloading {
    public static void main(String[] args) {
        int [] number = {1,2,3,4};
        Arrays.sort(number);

        String[] str = {"b", "c","a"};
        Arrays.sort(str);
        System.out.println(Arrays.toString(str));

    }


}
